function shuffleArray(array) {
    for (let i = array.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [array[i], array[j]] = [array[j], array[i]];
    }
    return array;
}

// Function to get a random category and shorten the list to 25
function getRandomWordList() {
    // Pick a random category
    const randomCategoryIndex = Math.floor(Math.random() * wordLists.length);
    const selectedCategory = wordLists[randomCategoryIndex];

    // Shuffle the list within the category
    const shuffledList = shuffleArray([...selectedCategory.list]); // Use spread operator to avoid modifying the original array

    // Shorten the list to 25 items
    const shortenedList = shuffledList.slice(0, 25);

    return {
        listTitle: selectedCategory.listTitle,
        list: shortenedList
    };
}

